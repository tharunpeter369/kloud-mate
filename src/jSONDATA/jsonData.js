function convertToTreeData(spans) {
    const map = new Map();

    // Create a map of spanId to span object
    for (const span of spans) {
        map.set(span.spanId, span);
    }
    // Create a map of traceId to root spanId
    const traceMap = new Map();
    for (const span of spans) {
        if (!span.parentSpanId) {
            traceMap.set(span.traceId, span.spanId);
        }
    }
    // Build the tree structure
    const treeData = [];
    for (const span of spans) {
        if (span.parentSpanId) {
            const parentSpan = map.get(span.parentSpanId);
            if (parentSpan) {
                if (!parentSpan.children) {
                    parentSpan.children = [];
                }
                parentSpan.children.push(span);
            }
        } else if (!traceMap.has(span.spanId)) {
            treeData.push(span);
        }
    }
    return treeData;
}

